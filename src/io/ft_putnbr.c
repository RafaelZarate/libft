#include "libft.h"

void	ft_putnbr(intmax_t n)
{
	if (n < 0)
	{
		write(STDOUT, "-", 1);
		n *= -1;
	}
	if (n < 10)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
