#include "libft.h"

void	ft_putnbr_li(long n)
{
	// if (n == -9223372036854775808)
	// {
	// 	ft_putstr("-922337203685");
	// 	return ;
	// }
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n < 10)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
